def add(num1, num2)
  num1 + num2
end
def subtract(num1, num2)
  num1 - num2 
end
def multiply(num1, num2)
  num1 * num2
end
def divide(num1, num2)
  num1.to_f / num2
end

add(10,2)      # => 12
subtract(10,2) # => 8
multiply(10,2) # => 20
divide(10,2)   # => 5.0 (*no* 5)
